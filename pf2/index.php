        <h4>Applying the Modifications</h4>

        <p>Level Bump for Adventures: Scenarios and quests are designed to challenge PCs
        whose average level is the lower of the two levels in a given subtier. Applying a
        level bump is a simple calculation:</p>

        <ul>
          <li>Increase every DC listed in the scenario by 1.</li>

          <li>Increase the attack modifiers, attack damage, spell damage, saving throw
          modifiers, skill modifiers, Perception modifiers, and ACs of all enemy
          creatures by 1.</li>

          <li>Increase the Hit Point totals of all enemy creatures by 10 or by 10%,
          whichever is higher.</li>
        </ul>

        <p>Level Bump for PCs: When a PC of the lowest legal level for an adventure plays
        in the high subtier, they also gain the level bump benefits listed above for the
        duration of the adventure, but instead applied to their character's Armor Class,
        DCs, modifiers, and Hit Points.</p>

        <h4>Hero Points</h4>

        <p>At the beginning of an adventure, each player receives 1 Hero Point. GMs can
        then hand out additional Hero Points throughout the session in recognition of the
        characters' deeds, at a rate of approximately 1 Hero Point per hour of play. When
        handing out Hero Points, try to spread them out throughout the table rather than
        giving most of the points to one player.</p>

        <p>Players who have earned GM glyphs can hand out one additional hero point per
        Glyph at the start of the adventure when boons are slotted.&nbsp; But no player
        can receive more than 1 hero point this way per adventure.&nbsp;</p>
