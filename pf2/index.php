        <h4 id="purchasing-guidelines">Purchasing Guidelines</h4>

        <p>You can always purchase the following items so long as you're in a
        settlement of at least 5,000 residents (and scenarios may sometimes provide
        additional allowances or limitations):</p>

        <ul>
          <li>Any common equipment in sanctioned Pathfinder content with an item level
          equal to or less than your character's level (minimum 2).</li>

          <li>Any uncommon equipment in sanctioned Pathfinder content with an item level
          equal to or less than your character's level (minimum 2); your character must
          have access to this uncommon equipment such as through meeting its Access
          condition.</li>

          <li>Any equipment listed on your character's Chronicle sheets with an item
          level equal to or less than your character's level + 2. Some items found on
          Chronicle sheets are available for purchase only a limited number of
          times.</li>

          <li>Any items and services purchased with Fame.</li>
        </ul>

        <p>Note that accumulating Infamy can reduce the character's effective level when
        purchasing equipment.</p>


        <h4>Advancement Speed</h4>

        <p>Pathfinder Society characters can choose between two different advancement
        speeds, allowing them to gain levels and rewards more quickly or to slow down
        their advancement (such as to extend their character's career or allow new
        players to catch up). It is entirely possible for different PCs at a table to use
        different advancement speeds with no impact on the game. At the beginning of the
        adventure, you choose which of the two advancement speeds to use.</p>

        <p><strong>Standard Advancement:</strong> You gain the full XP, gp, Downtime,
        Fame, and Reputation rewards earned in the adventure.</p>

        <p><strong>Slow Advancement:</strong> You gain half of the XP, gp, Downtime,
        Fame, and Reputation rewards earned in the adventure.</p>

        <h4>Experience Points</h4>

        <p>Each time you play an adventure, your character will gain Experience Points
        (XP). Typically, you gain 1 XP for completing a quest, 4 XP for completing a
        scenario, or even 12 XP for completing most sanctioned Pathfinder Adventure or
        Adventure Path volumes. For every 12 XP your character earns, they advance 1
        level. You cannot delay increasing your character's level once they earn the
        requisite number of XP.</p>

        <h4 id="fame-and-reputation">Fame and Reputation</h4>

        <p>A quest can grant up to 1 Fame and 1 Reputation. A scenario can grant up to 4
        Fame and 4 Reputation, though scenarios with the Faction tag typically have an
        opportunity to gain additional Reputation with the associated faction. Sanctioned
        Pathfinder Adventures and Pathfinder Adventure Path volumes typically grant up to
        12 Fame and 12 Reputation.</p>


        <h4>Treasure</h4>

        <p>At the end of the adventure, the GM crosses out any items the PCs did not
        discover.</p>

        <h4 id="downtime">Downtime</h4>

        <p>A quest grants 2 days of Downtime, a scenario grants 8 days, and a sanctioned
        Pathfinder Adventure or Adventure Path typically grants 24 days of Downtime. If
        your character is a field-commissioned agent (rather than having trained with the
        Scrolls, Spells, and Swords Pathfinder Schools), they earn an additional 50% as
        many days of Downtime, earning 3, 12, and 36 days respectively.</p>

        <p>A character can use Downtime in a variety of ways, including the Crafting,
        Earn Income, and retraining options in the <em>Pathfinder Core Rulebook</em>.
        Downtime must be spent at the end of the session or it expires; it cannot be
        stockpiled for later use. However, players can spend their Downtime to start or
        continue an activity such as Crafting an item that requires multiple adventures'
        worth of Downtime to complete.</p>        

 
